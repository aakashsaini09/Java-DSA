package algos;

public class Arith {
    public int SumOfTwo(int a, int b){
        return a + b;
    }
    public int MinusOfTwo(int a, int b){
        return a - b;
    }
    public int DivOfTwo(int a, int b){
        return a / b;
    }
    public int SubOfTwo(int a, int b){
        return a * b;
    }
}
